#Pede um número para o usuario.
num = int(input('Digite um número: '))

#Mostra o antecessor e o sucessor do número digitado pelo usuario
#num-1 -> Descobre o antecessor subtraindo menos um
#num+2 -> Descobre o sucessor somando mais um
print(f'Analisando o valor {num}, seu antecessor é {num-1} e seu sucessor é {num+1}')
